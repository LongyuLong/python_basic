# 처리 조건 : 특정 년도는 4의 배수이고 100의 배수가 아니거나 400의 배수이면 윤년이다.

# 실행 2회 실행 예)
# 연도 입력:2020
# 2020년은 윤년
# 연도 입력:2022
# 2020년은 평년 (배점:10)

year = 0
year = int(input("연도를 입력하세요: "))

if year % 4 == 0 or year % 400 == 0 and year % 100 != 0:
    print(f'{year}년은 윤년')
else:
    print(f'{year}년은 평년')









